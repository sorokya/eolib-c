static ProtocolDef parse_protocol_file(const char *path)
{
    ProtocolDef protocol = {0};
    protocol.path = xstrdup(path);

    xmlDoc *doc = xmlReadFile(path, NULL, XML_PARSE_NOBLANKS);
    if (!doc)
    {
        fprintf(stderr, "Failed to parse XML: %s\n", path);
        exit(1);
    }

    xmlNode *root = xmlDocGetRootElement(doc);
    if (!root)
    {
        fprintf(stderr, "Empty XML: %s\n", path);
        xmlFreeDoc(doc);
        exit(1);
    }

    for (xmlNode *node = root->children; node; node = node->next)
    {
        if (node->type != XML_ELEMENT_NODE)
        {
            continue;
        }
        if (xmlStrEqual(node->name, (const xmlChar *)"enum"))
        {
            protocol_enums_push(&protocol, parse_enum(node));
        }
        else if (xmlStrEqual(node->name, (const xmlChar *)"struct"))
        {
            protocol_structs_push(&protocol, parse_struct(node));
        }
        else if (xmlStrEqual(node->name, (const xmlChar *)"packet"))
        {
            protocol_packets_push(&protocol, parse_packet(node));
        }
    }

    xmlFreeDoc(doc);
    return protocol;
}

static void write_doc_comment(FILE *out, const char *comment, const char *indent)
{
    if (!comment || comment[0] == '\0')
    {
        return;
    }
    char *copy = xstrdup(comment);
    char *line = strtok(copy, "\n");
    fprintf(out, "%s/**\n", indent);
    while (line)
    {
        char *start = line;
        while (*start && isspace((unsigned char)*start))
        {
            start++;
        }
        char *end = start + strlen(start);
        while (end > start && isspace((unsigned char)*(end - 1)))
        {
            end--;
        }
        *end = '\0';
        if (start[0] != '\0')
        {
            fprintf(out, "%s * %s\n", indent, start);
        }
        line = strtok(NULL, "\n");
    }
    fprintf(out, "%s */\n", indent);
    free(copy);
}

static void write_enum_def(FILE *header, FILE *source, EnumDef *def)
{
    if (def->comment)
    {
        write_doc_comment(header, def->comment, "");
    }

    char *enum_prefix = to_upper_snake(def->name);
    fprintf(header, "typedef enum %s {\n", def->name);
    for (size_t i = 0; i < def->values_count; ++i)
    {
        EnumValue *value = &def->values[i];
        if (value->comment)
        {
            write_doc_comment(header, value->comment, "    ");
        }
        char *constant_name = to_upper_snake(value->name);
        fprintf(header, "    %s_%s = %d%s\n",
                enum_prefix,
                constant_name,
                value->value,
                i + 1 == def->values_count ? "" : ",");
        free(constant_name);
    }
    fprintf(header, "} %s;\n\n", def->name);
    free(enum_prefix);
}

