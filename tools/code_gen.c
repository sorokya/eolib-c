static int path_is_dir(const char *path)
{
    struct stat st;
    if (stat(path, &st) != 0)
    {
        return 0;
    }
    return (st.st_mode & S_IFDIR) != 0;
}

static int path_exists(const char *path)
{
    struct stat st;
    return stat(path, &st) == 0;
}

static void string_list_push_unique(StringList *paths, const char *value)
{
    if (!string_list_contains(paths, value))
    {
        string_list_push(paths, value);
    }
}

static void list_subdirs(const char *base_path, StringList *out)
{
#if defined(_WIN32)
    char search[EOLIB_PATH_MAX];
    WIN32_FIND_DATAA data;
    HANDLE handle;

    snprintf(search, sizeof(search), "%s\\*", base_path);
    handle = FindFirstFileA(search, &data);
    if (handle == INVALID_HANDLE_VALUE)
    {
        return;
    }

    do
    {
        const char *name = data.cFileName;
        if (strcmp(name, ".") == 0 || strcmp(name, "..") == 0)
        {
            continue;
        }
        if (data.dwFileAttributes & FILE_ATTRIBUTE_DIRECTORY)
        {
            char path[EOLIB_PATH_MAX];
            snprintf(path, sizeof(path), "%s/%s", base_path, name);
            string_list_push_unique(out, path);
        }
    } while (FindNextFileA(handle, &data) != 0);

    FindClose(handle);
#else
    DIR *dir = opendir(base_path);
    if (!dir)
    {
        return;
    }

    struct dirent *entry;
    while ((entry = readdir(dir)) != NULL)
    {
        if (strcmp(entry->d_name, ".") == 0 || strcmp(entry->d_name, "..") == 0)
        {
            continue;
        }

        char path[EOLIB_PATH_MAX];
        snprintf(path, sizeof(path), "%s/%s", base_path, entry->d_name);
        if (path_is_dir(path))
        {
            string_list_push_unique(out, path);
        }
    }
    closedir(dir);
#endif
}

static void append_glob_results(StringList *paths, const char *pattern)
{
#if defined(_WIN32)
    if (strcmp(pattern, "eo-protocol/xml/protocol.xml") == 0)
    {
        if (path_exists(pattern))
        {
            string_list_push_unique(paths, pattern);
        }
        return;
    }

    if (strcmp(pattern, "eo-protocol/xml/*/protocol.xml") == 0)
    {
        StringList dirs = {0};
        list_subdirs("eo-protocol/xml", &dirs);
        for (size_t i = 0; i < dirs.count; ++i)
        {
            char path[EOLIB_PATH_MAX];
            snprintf(path, sizeof(path), "%s/protocol.xml", dirs.items[i]);
            if (path_exists(path))
            {
                string_list_push_unique(paths, path);
            }
        }
        return;
    }

    if (strcmp(pattern, "eo-protocol/xml/*/*/protocol.xml") == 0)
    {
        StringList level_one = {0};
        list_subdirs("eo-protocol/xml", &level_one);
        for (size_t i = 0; i < level_one.count; ++i)
        {
            StringList level_two = {0};
            list_subdirs(level_one.items[i], &level_two);
            for (size_t j = 0; j < level_two.count; ++j)
            {
                char path[EOLIB_PATH_MAX];
                snprintf(path, sizeof(path), "%s/protocol.xml", level_two.items[j]);
                if (path_exists(path))
                {
                    string_list_push_unique(paths, path);
                }
            }
        }
        return;
    }
#else
    glob_t matches;
    memset(&matches, 0, sizeof(matches));
    if (glob(pattern, GLOB_TILDE, NULL, &matches) == 0)
    {
        for (size_t i = 0; i < matches.gl_pathc; ++i)
        {
            string_list_push_unique(paths, matches.gl_pathv[i]);
        }
    }
    globfree(&matches);
#endif
}

static const char *c_keywords[] = {
    "auto", "break", "case", "char", "const", "continue", "default",
    "do", "double", "else", "enum", "extern", "float", "for",
    "goto", "if", "inline", "int", "long", "register", "restrict",
    "return", "short", "signed", "sizeof", "static", "struct", "switch",
    "typedef", "union", "unsigned", "void", "volatile", "while", "_Bool",
    "_Complex", "_Imaginary"};

static int is_c_keyword(const char *value)
{
    for (size_t i = 0; i < sizeof(c_keywords) / sizeof(c_keywords[0]); ++i)
    {
        if (strcmp(value, c_keywords[i]) == 0)
        {
            return 1;
        }
    }
    return 0;
}

static char *sanitize_identifier(const char *value)
{
    if (!value)
    {
        return xstrdup("_");
    }

    size_t len = strlen(value);
    char *out = xmalloc(len * 2 + 2);
    size_t out_len = 0;

    if (value[0] >= '0' && value[0] <= '9')
    {
        out[out_len++] = '_';
    }

    for (size_t i = 0; i < len; ++i)
    {
        char ch = value[i];
        if ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z') ||
            (ch >= '0' && ch <= '9') || ch == '_')
        {
            out[out_len++] = ch;
        }
        else
        {
            out[out_len++] = '_';
        }
    }

    out[out_len] = '\0';
    if (is_c_keyword(out))
    {
        char *prefixed = xmalloc(out_len + 3);
        snprintf(prefixed, out_len + 3, "_%s", out);
        free(out);
        return prefixed;
    }

    return out;
}

static char *to_snake_case(const char *value)
{
    if (!value)
    {
        return xstrdup("_");
    }

    size_t len = strlen(value);
    char *out = xmalloc(len * 2 + 2);
    size_t out_len = 0;

    for (size_t i = 0; i < len; ++i)
    {
        char ch = value[i];
        if (ch >= 'A' && ch <= 'Z')
        {
            if (i > 0 && out[out_len - 1] != '_')
            {
                out[out_len++] = '_';
            }
            out[out_len++] = (char)(ch - 'A' + 'a');
        }
        else if (ch == '-' || ch == ' ')
        {
            if (out_len > 0 && out[out_len - 1] != '_')
            {
                out[out_len++] = '_';
            }
        }
        else
        {
            out[out_len++] = ch;
        }
    }

    out[out_len] = '\0';
    char *sanitized = sanitize_identifier(out);
    free(out);
    return sanitized;
}

static char *to_upper_snake(const char *value)
{
    char *snake = to_snake_case(value);
    for (size_t i = 0; snake[i]; ++i)
    {
        if (snake[i] >= 'a' && snake[i] <= 'z')
        {
            snake[i] = (char)(snake[i] - 'a' + 'A');
        }
    }
    return snake;
}

static int parse_bool_attr(xmlNode *node, const char *name, int default_value)
{
    xmlChar *value = xmlGetProp(node, (const xmlChar *)name);
    if (!value)
    {
        return default_value;
    }
    int result = (xmlStrcasecmp(value, (const xmlChar *)"true") == 0 ||
                  xmlStrcasecmp(value, (const xmlChar *)"1") == 0)
                     ? 1
                     : 0;
    xmlFree(value);
    return result;
}

static int parse_optional_bool_attr(xmlNode *node, const char *name, const char *fallback_name,
                                    int default_value)
{
    xmlChar *value = xmlGetProp(node, (const xmlChar *)name);
    if (!value && fallback_name)
    {
        value = xmlGetProp(node, (const xmlChar *)fallback_name);
    }
    if (!value)
    {
        return default_value;
    }
    int result = (xmlStrcasecmp(value, (const xmlChar *)"true") == 0 ||
                  xmlStrcasecmp(value, (const xmlChar *)"1") == 0)
                     ? 1
                     : 0;
    xmlFree(value);
    return result;
}

