static void *xmalloc(size_t size)
{
    void *ptr = malloc(size);
    if (!ptr)
    {
        fprintf(stderr, "Out of memory\n");
        exit(1);
    }
    return ptr;
}

static char *xstrdup(const char *value)
{
    if (!value)
    {
        return NULL;
    }
    char *dup = strdup(value);
    if (!dup)
    {
        fprintf(stderr, "Out of memory\n");
        exit(1);
    }
    return dup;
}

