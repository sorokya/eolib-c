/*
 * Map from (struct_name, field_name) to static array size.
 * Used to distinguish fixed-size C arrays like SomeType name[N] from dynamic typed arrays.
 */
typedef struct
{
    char *struct_name;
    char *field_name;
    int size;
} FixedArrayEntry;

typedef struct
{
    FixedArrayEntry *entries;
    size_t count;
    size_t capacity;
} FixedArrayMap;

static void fixed_array_map_push(FixedArrayMap *map, const char *struct_name,
                                 const char *field_name, int size)
{
    if (map->count >= map->capacity)
    {
        map->capacity = ARRAY_GROW_CAPACITY(map->capacity);
        map->entries = realloc(map->entries, map->capacity * sizeof(FixedArrayEntry));
        if (!map->entries)
        {
            fprintf(stderr, "Out of memory\n");
            exit(1);
        }
    }
    map->entries[map->count].struct_name = xstrdup(struct_name ? struct_name : "");
    map->entries[map->count].field_name = xstrdup(field_name ? field_name : "");
    map->entries[map->count].size = size;
    map->count++;
}

/* Returns the static array size if (struct_name, field_name) is a fixed-size C array, 0 otherwise. */
static int fixed_array_lookup(const FixedArrayMap *map, const char *struct_name,
                              const char *field_name)
{
    if (!struct_name || !field_name)
        return 0;
    for (size_t i = 0; i < map->count; ++i)
    {
        if (strcmp(map->entries[i].struct_name, struct_name) == 0 &&
            strcmp(map->entries[i].field_name, field_name) == 0)
        {
            return map->entries[i].size;
        }
    }
    return 0;
}

static void collect_fixed_arrays_from_elements(const char *struct_name,
                                               ElementList *elements,
                                               FixedArrayMap *map);

static void collect_fixed_arrays_from_elements(const char *struct_name,
                                               ElementList *elements,
                                               FixedArrayMap *map)
{
    for (size_t i = 0; i < elements->elements_count; ++i)
    {
        StructElement *el = &elements->elements[i];
        if (el->kind == ELEMENT_ARRAY)
        {
            ArrayDef *arr = &el->as.array;
            if (arr->name && is_static_length(arr->length))
            {
                char *field_name = to_snake_case(arr->name);
                int size = atoi(arr->length);
                fixed_array_map_push(map, struct_name, field_name, size);
                free(field_name);
            }
        }
        else if (el->kind == ELEMENT_CHUNKED)
        {
            collect_fixed_arrays_from_elements(struct_name, &el->as.chunked, map);
        }
        else if (el->kind == ELEMENT_SWITCH)
        {
            SwitchDef *sw = &el->as.sw;
            for (size_t c = 0; c < sw->cases_count; ++c)
            {
                if (sw->cases[c].has_elements)
                {
                    collect_fixed_arrays_from_elements(struct_name,
                                                       &sw->cases[c].elements, map);
                }
            }
        }
    }
}

static void build_fixed_array_map(ProtocolDef *protocols, size_t protocol_count,
                                  FixedArrayMap *map)
{
    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *proto = &protocols[p];
        for (size_t s = 0; s < proto->structs_count; ++s)
        {
            collect_fixed_arrays_from_elements(proto->structs[s].name,
                                               &proto->structs[s].elements, map);
        }
        for (size_t pk = 0; pk < proto->packets_count; ++pk)
        {
            PacketDef *pkt = &proto->packets[pk];
            const char *dir_suffix = (strstr(proto->path, "/server/") ||
                                      strstr(proto->path, "/server/protocol.xml"))
                                         ? "Server"
                                         : "Client";
            char pkt_struct[256];
            snprintf(pkt_struct, sizeof(pkt_struct), "%s%s%sPacket",
                     pkt->family, pkt->action, dir_suffix);
            collect_fixed_arrays_from_elements(pkt_struct, &pkt->elements, map);
        }
    }
}

/* Returns a pointer into 'type_str' past the last '::' separator, or type_str if none. */
static const char *strip_type_namespace(const char *type_str)
{
    const char *last = NULL;
    const char *p = type_str;
    while (*p)
    {
        if (p[0] == ':' && p[1] == ':')
        {
            last = p + 2;
            p += 2;
        }
        else
        {
            ++p;
        }
    }
    return last ? last : type_str;
}

/* Converts snake_case to PascalCase. Returns malloc'd string. */
static char *to_pascal_case(const char *snake)
{
    if (!snake)
        return xstrdup("");
    size_t len = strlen(snake);
    char *out = xmalloc(len + 2);
    size_t out_len = 0;
    int capitalize = 1;
    for (size_t i = 0; i < len; ++i)
    {
        char ch = snake[i];
        if (ch == '_')
        {
            capitalize = 1;
        }
        else if (capitalize)
        {
            out[out_len++] = (char)(ch >= 'a' && ch <= 'z' ? ch - 'a' + 'A' : ch);
            capitalize = 0;
        }
        else
        {
            out[out_len++] = ch;
        }
    }
    out[out_len] = '\0';
    return out;
}

/* Fills 'out' with the names of all named struct types across all protocols. */
static void collect_known_structs(ProtocolDef *protocols, size_t protocol_count,
                                  StringList *out)
{
    for (size_t p = 0; p < protocol_count; ++p)
    {
        for (size_t s = 0; s < protocols[p].structs_count; ++s)
        {
            string_list_push_unique(out, protocols[p].structs[s].name);
        }
    }
}

/*
 * Given a type like "LoginReplyReplyCodeDataOk" and field name "reply_code_data",
 * derives the C union variant name (e.g. "ok").
 * Returns a malloc'd string.
 */
static char *extract_union_variant(const char *local_type, const char *field_name)
{
    char *pascal_field = to_pascal_case(field_name);
    const char *pos = strstr(local_type, pascal_field);
    char *variant;
    if (pos)
    {
        const char *suffix = pos + strlen(pascal_field);
        variant = to_snake_case(suffix);
    }
    else
    {
        /* Fallback: use the entire local_type suffix lowercased */
        variant = to_snake_case(local_type);
    }
    free(pascal_field);
    return variant;
}

/* Writes a C string literal with proper escaping. */
static void write_escaped_c_string(FILE *f, const char *s)
{
    fputc('"', f);
    for (; *s; ++s)
    {
        switch (*s)
        {
        case '"':
            fputs("\\\"", f);
            break;
        case '\\':
            fputs("\\\\", f);
            break;
        case '\n':
            fputs("\\n", f);
            break;
        case '\r':
            fputs("\\r", f);
            break;
        case '\t':
            fputs("\\t", f);
            break;
        default:
            fputc(*s, f);
            break;
        }
    }
    fputc('"', f);
}

/* Forward declaration */
static void write_property_assertions(FILE *f, struct json_object *properties,
                                      const char *c_path, const char *assert_path,
                                      const char *current_struct,
                                      StringList *known_structs,
                                      FixedArrayMap *fixed_arrays);

static void write_single_property_assertions(FILE *f, struct json_object *prop,
                                             const char *c_path,
                                             const char *assert_path,
                                             const char *current_struct,
                                             StringList *known_structs,
                                             FixedArrayMap *fixed_arrays)
{
    struct json_object *name_obj = NULL, *type_obj = NULL;
    struct json_object *value_obj = NULL, *children_obj = NULL;
    json_object_object_get_ex(prop, "name", &name_obj);
    json_object_object_get_ex(prop, "type", &type_obj);
    json_object_object_get_ex(prop, "value", &value_obj);
    json_object_object_get_ex(prop, "children", &children_obj);

    const char *name = name_obj ? json_object_get_string(name_obj) : NULL;
    const char *type_str = type_obj ? json_object_get_string(type_obj) : NULL;

    /* Skip unnamed entries (raw array elements handled by their parent) */
    if (!name)
        return;

    /* Optional fields are now pointers — dereference for scalar assertions */
    struct json_object *optional_obj = NULL;
    json_object_object_get_ex(prop, "optional", &optional_obj);
    int is_optional = optional_obj && json_object_get_boolean(optional_obj);

    /* Build the C accessor expression and a human-readable assertion label */
    char c_expr[2048];
    if (is_optional && type_str &&
        strcmp(type_str, "string") != 0 && type_str[0] != '[')
        snprintf(c_expr, sizeof(c_expr), "*%s%s", c_path, name);
    else
        snprintf(c_expr, sizeof(c_expr), "%s%s", c_path, name);

    char label[1024];
    snprintf(label, sizeof(label), "%s %s", assert_path, name);

    /* --- Emit value assertion --- */
    if (value_obj && type_str)
    {
        if (strcmp(type_str, "string") == 0)
        {
            const char *str_val = json_object_get_string(value_obj);
            fprintf(f, "    expect_equal_str(\"%s\", %s, ", label, c_expr);
            write_escaped_c_string(f, str_val ? str_val : "");
            fprintf(f, ");\n");
        }
        else if (strcmp(type_str, "bool") == 0)
        {
            int bool_val = json_object_get_boolean(value_obj);
            fprintf(f, "    expect_equal_int(\"%s\", (int)%s, %d);\n",
                    label, c_expr, bool_val);
        }
        else if (strcmp(type_str, "[]byte") == 0)
        {
            const char *b64 = json_object_get_string(value_obj);
            if (b64 && *b64)
            {
                uint8_t *blob = NULL;
                int blob_len = base64_decode(b64, &blob);
                if (blob_len >= 0)
                {
                    fprintf(f, "    {\n");
                    fprintf(f, "        static const uint8_t expected_bytes[] = ");
                    write_byte_array_literal(f, blob, (size_t)blob_len);
                    fprintf(f, ";\n");
                    fprintf(f,
                            "        expect_equal_bytes(\"%s\", %s, expected_bytes, %d);\n",
                            label, c_expr, blob_len);
                    fprintf(f,
                            "        expect_equal_size(\"%s length\", %s%s_length, %zu);\n",
                            label, c_path, name, (size_t)blob_len);
                    fprintf(f, "    }\n");
                    free(blob);
                }
            }
        }
        else
        {
            /* int, byte, or enum type -- treat as numeric */
            int64_t num_val = json_object_get_int64(value_obj);
            fprintf(f, "    expect_equal_int(\"%s\", (int)%s, %d);\n",
                    label, c_expr, (int)num_val);
        }
    }

    /* --- Emit children assertions --- */
    if (type_str && type_str[0] == '[' && type_str[1] == ']' && strcmp(type_str + 2, "byte") != 0)
    {
        int child_count = children_obj ? json_object_array_length(children_obj) : 0;
        const char *elem_type = type_str + 2;

        if (strcmp(elem_type, "int") == 0)
        {
            /* []int: fixed-size C array or dynamic typed array */
            int fixed_size = fixed_array_lookup(fixed_arrays, current_struct, name);
            if (fixed_size > 0)
            {
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_val = NULL;
                    json_object_object_get_ex(child, "value", &child_val);
                    if (child_val)
                    {
                        int64_t v = json_object_get_int64(child_val);
                        fprintf(f, "    expect_equal_int(\"%s[%d]\", (int)%s[%d], %d);\n",
                                label, j, c_expr, j, (int)v);
                    }
                }
            }
            else
            {
                fprintf(f, "    expect_equal_size(\"%s length\", %s%s_length, %d);\n",
                        label, c_path, name, child_count);
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_val = NULL;
                    json_object_object_get_ex(child, "value", &child_val);
                    if (child_val)
                    {
                        int64_t v = json_object_get_int64(child_val);
                        fprintf(f, "    expect_equal_int(\"%s[%d]\", (int)%s[%d], %d);\n",
                                label, j, c_expr, j, (int)v);
                    }
                }
            }
        }
        else if (strcmp(elem_type, "string") == 0)
        {
            /* []string: fixed-size C array or dynamic typed array */
            int fixed_size = fixed_array_lookup(fixed_arrays, current_struct, name);
            if (fixed_size > 0)
            {
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_val = NULL;
                    json_object_object_get_ex(child, "value", &child_val);
                    if (child_val)
                    {
                        const char *sv = json_object_get_string(child_val);
                        fprintf(f, "    expect_equal_str(\"%s[%d]\", %s[%d], ",
                                label, j, c_expr, j);
                        write_escaped_c_string(f, sv ? sv : "");
                        fprintf(f, ");\n");
                    }
                }
            }
            else
            {
                fprintf(f, "    expect_equal_size(\"%s length\", %s%s_length, %d);\n",
                        label, c_path, name, child_count);
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_val = NULL;
                    json_object_object_get_ex(child, "value", &child_val);
                    if (child_val)
                    {
                        const char *sv = json_object_get_string(child_val);
                        fprintf(f, "    expect_equal_str(\"%s[%d]\", %s[%d], ",
                                label, j, c_expr, j);
                        write_escaped_c_string(f, sv ? sv : "");
                        fprintf(f, ");\n");
                    }
                }
            }
        }
        else
        {
            /* []SomeName: fixed-size C array or dynamic typed array of structs */
            const char *local_elem = strip_type_namespace(elem_type);
            int fixed_size = fixed_array_lookup(fixed_arrays, current_struct, name);

            if (fixed_size > 0)
            {
                /* Fixed-size C array: SomeType name[N] -- no length field */
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_children = NULL;
                    json_object_object_get_ex(child, "children", &child_children);

                    char elem_c_path[2048];
                    snprintf(elem_c_path, sizeof(elem_c_path), "%s%s[%d].", c_path, name, j);

                    char elem_label[1024];
                    snprintf(elem_label, sizeof(elem_label), "%s[%d]", label, j);

                    if (child_children)
                    {
                        write_property_assertions(f, child_children, elem_c_path,
                                                  elem_label, local_elem,
                                                  known_structs, fixed_arrays);
                    }
                }
            }
            else
            {
                /* Dynamic typed array: always assert length, even when children absent */
                fprintf(f, "    expect_equal_size(\"%s length\", %s%s_length, %d);\n",
                        label, c_path, name, child_count);
                for (int j = 0; j < child_count; ++j)
                {
                    struct json_object *child = json_object_array_get_idx(children_obj, j);
                    struct json_object *child_children = NULL;
                    json_object_object_get_ex(child, "children", &child_children);

                    char elem_c_path[2048];
                    snprintf(elem_c_path, sizeof(elem_c_path), "%s[%d].", c_expr, j);

                    char elem_label[1024];
                    snprintf(elem_label, sizeof(elem_label), "%s[%d]", label, j);

                    if (child_children)
                    {
                        write_property_assertions(f, child_children, elem_c_path,
                                                  elem_label, local_elem,
                                                  known_structs, fixed_arrays);
                    }
                }
            }
        }
    }
    else if (children_obj && type_str)
    {
        int child_count = json_object_array_length(children_obj);
        (void)child_count;

        /* Struct or union-pointer type */
        const char *local_type = strip_type_namespace(type_str);
        char child_c_path[2048];
        const char *child_struct;

        if (string_list_contains(known_structs, local_type))
        {
            /* Inline named struct: access with "." */
            snprintf(child_c_path, sizeof(child_c_path), "%s%s.", c_path, name);
            child_struct = local_type;
        }
        else
        {
            /* Anonymous union sub-struct: access with "->variant." */
            char *variant = extract_union_variant(local_type, name);
            snprintf(child_c_path, sizeof(child_c_path),
                     "%s%s->%s.", c_path, name, variant);
            free(variant);
            /* Fixed arrays inside anonymous union sub-structs are stored under the
             * parent struct name in FixedArrayMap, so keep current_struct. */
            child_struct = current_struct;
        }

        write_property_assertions(f, children_obj, child_c_path, label,
                                  child_struct, known_structs, fixed_arrays);
    }
}

static void write_property_assertions(FILE *f, struct json_object *properties,
                                      const char *c_path, const char *assert_path,
                                      const char *current_struct,
                                      StringList *known_structs,
                                      FixedArrayMap *fixed_arrays)
{
    if (!properties)
        return;
    int n = json_object_array_length(properties);
    for (int i = 0; i < n; ++i)
    {
        struct json_object *prop = json_object_array_get_idx(properties, i);
        write_single_property_assertions(f, prop, c_path, assert_path,
                                         current_struct, known_structs, fixed_arrays);
    }
}

static void write_packet_tests(ProtocolDef *protocols, size_t protocol_count)
{
    if (ensure_dir("tests") != 0)
    {
        fprintf(stderr, "Failed to create tests directory\n");
        return;
    }

    FILE *f = fopen("tests/packet_tests.c", "w");
    if (!f)
    {
        fprintf(stderr, "Failed to open tests/packet_tests.c for writing\n");
        return;
    }

    FILE *list_file = fopen("tests/packet_tests.list", "w");
    if (!list_file)
    {
        fprintf(stderr, "Failed to open tests/packet_tests.list for writing\n");
        fclose(f);
        return;
    }

    PacketStorageMap map = build_packet_storage_map(protocols, protocol_count);

    StringList known_structs = {0};
    collect_known_structs(protocols, protocol_count, &known_structs);

    FixedArrayMap fixed_arrays = {0};
    build_fixed_array_map(protocols, protocol_count, &fixed_arrays);

    fprintf(f, "%s", CODEGEN_WARNING);
    fprintf(f, "#include \"test_utils.h\"\n");
    fprintf(f, "#include \"data.h\"\n");
    fprintf(f, "#include \"protocol.h\"\n");
    fprintf(f, "#include <stdio.h>\n");
    fprintf(f, "#include <stdlib.h>\n");
    fprintf(f, "#include <string.h>\n\n");

    StringList test_functions = {0};
    StringList test_case_names = {0};
    const char *dirs[] = {"client", "server"};
    for (int d = 0; d < 2; ++d)
    {
        char dir_path[256];
        snprintf(dir_path, sizeof(dir_path), "eo-captured-packets/%s", dirs[d]);

        StringList json_paths = {0};
        list_json_files_in_dir(dir_path, &json_paths);

        for (size_t i = 0; i < json_paths.count; ++i)
        {
            const char *path = json_paths.items[i];

            struct json_object *root = json_object_from_file(path);
            if (!root)
            {
                fprintf(stderr, "Failed to parse JSON: %s\n", path);
                continue;
            }

            struct json_object *family_obj = NULL, *action_obj = NULL, *expected_obj = NULL;
            struct json_object *properties_obj = NULL;
            if (!json_object_object_get_ex(root, "family", &family_obj) ||
                !json_object_object_get_ex(root, "action", &action_obj) ||
                !json_object_object_get_ex(root, "expected", &expected_obj))
            {
                fprintf(stderr, "Missing fields in JSON: %s\n", path);
                json_object_put(root);
                continue;
            }

            const char *family = json_object_get_string(family_obj);
            const char *action = json_object_get_string(action_obj);
            const char *expected_b64 = json_object_get_string(expected_obj);
            json_object_object_get_ex(root, "properties", &properties_obj);

            const char *suffix = d == 0 ? "Client" : "Server";
            char packet_name[256];
            snprintf(packet_name, sizeof(packet_name), "%s%s%sPacket",
                     family, action, suffix);

            int has_storage = packet_storage_map_lookup(&map, packet_name);

            uint8_t *expected_bytes = NULL;
            int expected_len = base64_decode(expected_b64, &expected_bytes);

            const char *basename = strrchr(path, '/');
            basename = basename ? basename + 1 : path;
            size_t stem_len = strlen(basename);
            if (stem_len > 5 && strcmp(basename + stem_len - 5, ".json") == 0)
                stem_len -= 5;
            char *stem = (char *)malloc(stem_len + 1);
            memcpy(stem, basename, stem_len);
            stem[stem_len] = '\0';

            char fn_name[512];
            snprintf(fn_name, sizeof(fn_name), "test_packet_%s_%s", dirs[d], stem);

            const char *name_start = fn_name;
            if (strncmp(name_start, "test_", 5) == 0)
                name_start += 5;
            string_list_push(&test_case_names, name_start);
            fprintf(list_file, "%s\n", name_start);

            fprintf(f, "static void %s(void)\n{\n", fn_name);

            if (expected_len > 0)
            {
                fprintf(f, "    static const uint8_t expected[] = ");
                write_byte_array_literal(f, expected_bytes, (size_t)expected_len);
                fprintf(f, ";\n");
                fprintf(f, "    size_t expected_len = sizeof(expected);\n");
                fprintf(f, "    EoReader reader = eo_reader_init(expected, expected_len);\n");
            }
            else
            {
                fprintf(f, "    size_t expected_len = 0;\n");
                fprintf(f, "    EoReader reader = eo_reader_init(NULL, 0);\n");
            }
            fprintf(f, "\n");

            int has_heap = packet_heap_map_lookup(&map, packet_name);

            if (has_storage)
            {
                fprintf(f, "    %s packet;\n", packet_name);
                fprintf(f,
                        "    int deser_result = %s_deserialize(&packet, &reader);\n",
                        packet_name);
                fprintf(f,
                        "    expect(\"%s_deserialize\", deser_result != -1);\n",
                        packet_name);
                fprintf(f, "    if (deser_result == -1) return;\n");
                fprintf(f, "\n");
                if (properties_obj)
                {
                    write_property_assertions(f, properties_obj, "packet.",
                                              name_start, packet_name,
                                              &known_structs, &fixed_arrays);
                    fprintf(f, "\n");
                }
                fprintf(f, "    EoWriter writer = eo_writer_init_with_capacity("
                           "expected_len > 0 ? expected_len : 1);\n");
                fprintf(f,
                        "    expect(\"%s_serialize\","
                        " %s_serialize(&packet, &writer) != -1);\n",
                        packet_name, packet_name);
                if (expected_len > 0)
                {
                    fprintf(f,
                            "    expect_equal_bytes(\"%s roundtrip\","
                            " writer.data, expected, expected_len);\n",
                            packet_name);
                }
                if (has_heap)
                {
                    fprintf(f, "    %s_free(&packet);\n", packet_name);
                }
            }
            else
            {
                fprintf(f,
                        "    int deser_result = %s_deserialize(&reader);\n",
                        packet_name);
                fprintf(f,
                        "    expect(\"%s_deserialize\", deser_result != -1);\n",
                        packet_name);
                fprintf(f, "    if (deser_result == -1) return;\n");
                fprintf(f, "\n");
                fprintf(f, "    EoWriter writer = eo_writer_init_with_capacity("
                           "expected_len > 0 ? expected_len : 1);\n");
                fprintf(f,
                        "    expect(\"%s_serialize\","
                        " %s_serialize(&writer) != -1);\n",
                        packet_name, packet_name);
            }

            fprintf(f, "    free(writer.data);\n");
            fprintf(f, "}\n\n");

            string_list_push(&test_functions, fn_name);

            free(stem);
            if (expected_bytes)
                free(expected_bytes);
            json_object_put(root);
        }
    }

    fprintf(f, "static const TestCase packet_tests[] = {\n");
    for (size_t i = 0; i < test_functions.count; ++i)
    {
        fprintf(f, "    {\"%s\", %s},\n",
                test_case_names.items[i], test_functions.items[i]);
    }
    fprintf(f, "};\n\n");
    fprintf(f, "int main(int argc, char **argv)\n{\n");
    fprintf(f, "    return run_tests(packet_tests, sizeof(packet_tests) / sizeof(packet_tests[0]), argc, argv);\n");
    fprintf(f, "}\n");

    fclose(f);
    fclose(list_file);
}

/* ===== Lua protocol binding code generation ===== */

static void write_lua_to_c_elements(FILE *f, ElementList *elements, EnumDef *enums,
    size_t ec, StructDef *structs, size_t sc,
    const char *idx_var, const char *out_expr, const char *out_access, int *depth);

static void write_c_to_lua_elements(FILE *f, ElementList *elements, EnumDef *enums,
    size_t ec, StructDef *structs, size_t sc,
    const char *t_var, const char *val_expr, const char *val_access, int *depth);

static void write_lua_to_c_elements(FILE *f, ElementList *elements, EnumDef *enums,
    size_t ec, StructDef *structs, size_t sc,
    const char *idx_var, const char *out_expr, const char *out_access, int *depth)
{
    for (size_t i = 0; i < elements->elements_count; ++i)
    {
        StructElement *element = &elements->elements[i];

        if (element->kind == ELEMENT_FIELD)
        {
            FieldDef *field = &element->as.field;
            if (!field->name || field->value != NULL)
                continue;

            char *snake_name = to_snake_case(field->name);
            char *type_name = normalize_type_name(field->data_type);

            fprintf(f, "    lua_getfield(L, %s, \"%s\");\n", idx_var, snake_name);

            if (strcmp(type_name, "bool") == 0)
            {
                if (field->optional)
                {
                    fprintf(f,
                        "    if (!lua_isnil(L, -1)) { %s%s%s = (bool*)malloc(sizeof(bool));"
                        " if (%s%s%s) *%s%s%s = lua_toboolean(L, -1) ? true : false; }\n",
                        out_expr, out_access, snake_name,
                        out_expr, out_access, snake_name,
                        out_expr, out_access, snake_name);
                }
                else
                {
                    fprintf(f, "    %s%s%s = lua_toboolean(L, -1) ? true : false;\n",
                        out_expr, out_access, snake_name);
                }
            }
            else if (strcmp(type_name, "string") == 0 || strcmp(type_name, "encoded_string") == 0)
            {
                fprintf(f,
                    "    { const char *__s = lua_tostring(L, -1); if (__s) { size_t __n = strlen(__s);"
                    " %s%s%s = (char*)malloc(__n+1); if (%s%s%s) memcpy(%s%s%s, __s, __n+1); } }\n",
                    out_expr, out_access, snake_name,
                    out_expr, out_access, snake_name,
                    out_expr, out_access, snake_name);
            }
            else if (strcmp(type_name, "blob") == 0)
            {
                fprintf(f,
                    "    { size_t __blen; const char *__bd = lua_tolstring(L, -1, &__blen);"
                    " if (__bd) { %s%s%s = (uint8_t*)malloc(__blen); if (%s%s%s)"
                    " { memcpy(%s%s%s, __bd, __blen); %s%s%s_length = __blen; } } }\n",
                    out_expr, out_access, snake_name,
                    out_expr, out_access, snake_name,
                    out_expr, out_access, snake_name,
                    out_expr, out_access, snake_name);
            }
            else if (enum_exists(enums, ec, type_name))
            {
                if (field->optional)
                {
                    fprintf(f,
                        "    if (!lua_isnil(L, -1)) { %s%s%s = (%s*)malloc(sizeof(%s));"
                        " if (%s%s%s) *%s%s%s = (%s)lua_tointeger(L, -1); }\n",
                        out_expr, out_access, snake_name, type_name, type_name,
                        out_expr, out_access, snake_name,
                        out_expr, out_access, snake_name, type_name);
                }
                else
                {
                    fprintf(f, "    %s%s%s = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, snake_name, type_name);
                }
            }
            else if (struct_exists(structs, sc, type_name))
            {
                if (struct_has_storage(structs, sc, type_name))
                {
                    if (field->optional)
                    {
                        fprintf(f,
                            "    if (!lua_isnil(L, -1) && lua_istable(L, -1)) {"
                            " %s%s%s = (%s*)calloc(1, sizeof(%s));"
                            " if (%s%s%s) lua_table_to_%s(L, lua_gettop(L), %s%s%s); }\n",
                            out_expr, out_access, snake_name, type_name, type_name,
                            out_expr, out_access, snake_name, type_name,
                            out_expr, out_access, snake_name);
                    }
                    else
                    {
                        fprintf(f,
                            "    if (lua_istable(L, -1)) lua_table_to_%s(L, lua_gettop(L), &%s%s%s);\n",
                            type_name, out_expr, out_access, snake_name);
                    }
                }
                /* struct without storage: just pop */
            }
            else
            {
                /* primitive numeric */
                const char *c_type = map_primitive_type(type_name);
                if (field->optional)
                {
                    fprintf(f,
                        "    if (!lua_isnil(L, -1)) { %s%s%s = (%s*)malloc(sizeof(%s));"
                        " if (%s%s%s) *%s%s%s = (%s)lua_tointeger(L, -1); }\n",
                        out_expr, out_access, snake_name, c_type, c_type,
                        out_expr, out_access, snake_name,
                        out_expr, out_access, snake_name, c_type);
                }
                else
                {
                    fprintf(f, "    %s%s%s = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, snake_name, c_type);
                }
            }

            fprintf(f, "    lua_pop(L, 1);\n");
            free(snake_name);
            free(type_name);
        }
        else if (element->kind == ELEMENT_ARRAY)
        {
            ArrayDef *array = &element->as.array;
            if (!array->name)
                continue;

            int d = *depth;
            (*depth)++;

            char *arr_name = to_snake_case(array->name);
            char *type_name = normalize_type_name(array->data_type);
            int is_static = is_static_length(array->length);

            int is_string_t = (strcmp(type_name, "string") == 0 ||
                               strcmp(type_name, "encoded_string") == 0);
            int is_enum_t = enum_exists(enums, ec, type_name);
            int is_struct_t = (!is_primitive_type(type_name) && !is_enum_t &&
                               struct_exists(structs, sc, type_name));

            const char *c_type;
            if (is_string_t)
                c_type = "char*";
            else if (strcmp(type_name, "blob") == 0)
                c_type = "uint8_t";
            else if (is_struct_t || is_enum_t)
                c_type = type_name;
            else
                c_type = map_primitive_type(type_name);

            fprintf(f, "    lua_getfield(L, %s, \"%s\");\n", idx_var, arr_name);
            fprintf(f,
                "    if (lua_istable(L, -1)) { int __eolib_arr_%d = lua_gettop(L);"
                " int __eolib_n_%d = (int)lua_rawlen(L, __eolib_arr_%d);\n",
                d, d, d);

            if (is_static)
            {
                fprintf(f,
                    "    int __eolib_lim_%d = %s; if (__eolib_n_%d > __eolib_lim_%d)"
                    " __eolib_n_%d = __eolib_lim_%d;\n",
                    d, array->length, d, d, d, d);
            }
            else
            {
                fprintf(f,
                    "    %s%s%s_length = (size_t)__eolib_n_%d;"
                    " %s%s%s = __eolib_n_%d > 0 ? (%s*)calloc((size_t)__eolib_n_%d, sizeof(%s)) : NULL;\n",
                    out_expr, out_access, arr_name, d,
                    out_expr, out_access, arr_name, d,
                    c_type, d, c_type);
            }

            fprintf(f,
                "    for (__eolib_i = 1; __eolib_i <= __eolib_n_%d; __eolib_i++) {"
                " lua_rawgeti(L, __eolib_arr_%d, __eolib_i);\n",
                d, d);

            /* Emit element assignment into arr[__eolib_i - 1] */
            if (is_string_t)
            {
                if (is_static)
                {
                    fprintf(f,
                        "    { const char *__s = lua_tostring(L, -1); if (__s) { size_t __n = strlen(__s);"
                        " %s%s%s[__eolib_i - 1] = (char*)malloc(__n+1);"
                        " if (%s%s%s[__eolib_i - 1]) memcpy(%s%s%s[__eolib_i - 1], __s, __n+1); } }\n",
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name);
                }
                else
                {
                    fprintf(f,
                        "    if (%s%s%s) { const char *__s = lua_tostring(L, -1); if (__s) {"
                        " size_t __n = strlen(__s); %s%s%s[__eolib_i - 1] = (char*)malloc(__n+1);"
                        " if (%s%s%s[__eolib_i - 1]) memcpy(%s%s%s[__eolib_i - 1], __s, __n+1); } }\n",
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name);
                }
            }
            else if (strcmp(type_name, "bool") == 0)
            {
                if (is_static)
                {
                    fprintf(f,
                        "    %s%s%s[__eolib_i - 1] = lua_toboolean(L, -1) ? true : false;\n",
                        out_expr, out_access, arr_name);
                }
                else
                {
                    fprintf(f,
                        "    if (%s%s%s) %s%s%s[__eolib_i - 1] = lua_toboolean(L, -1) ? true : false;\n",
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name);
                }
            }
            else if (is_struct_t)
            {
                if (struct_has_storage(structs, sc, type_name))
                {
                    if (is_static)
                    {
                        fprintf(f,
                            "    if (lua_istable(L, -1)) lua_table_to_%s(L, lua_gettop(L), &%s%s%s[__eolib_i - 1]);\n",
                            type_name, out_expr, out_access, arr_name);
                    }
                    else
                    {
                        fprintf(f,
                            "    if (%s%s%s && lua_istable(L, -1)) lua_table_to_%s(L, lua_gettop(L), &%s%s%s[__eolib_i - 1]);\n",
                            out_expr, out_access, arr_name, type_name,
                            out_expr, out_access, arr_name);
                    }
                }
                /* struct without storage: no assignment needed */
            }
            else if (is_enum_t)
            {
                if (is_static)
                {
                    fprintf(f,
                        "    %s%s%s[__eolib_i - 1] = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, arr_name, type_name);
                }
                else
                {
                    fprintf(f,
                        "    if (%s%s%s) %s%s%s[__eolib_i - 1] = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name, type_name);
                }
            }
            else
            {
                /* primitive */
                if (is_static)
                {
                    fprintf(f,
                        "    %s%s%s[__eolib_i - 1] = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, arr_name, c_type);
                }
                else
                {
                    fprintf(f,
                        "    if (%s%s%s) %s%s%s[__eolib_i - 1] = (%s)lua_tointeger(L, -1);\n",
                        out_expr, out_access, arr_name,
                        out_expr, out_access, arr_name, c_type);
                }
            }

            fprintf(f, "    lua_pop(L, 1); }\n"); /* close loop */
            fprintf(f, "    }\n"); /* close if (lua_istable) */
            fprintf(f, "    lua_pop(L, 1);\n");

            free(arr_name);
            free(type_name);
        }
        else if (element->kind == ELEMENT_SWITCH)
        {
            SwitchDef *sw = &element->as.sw;
            if (!switch_has_storage(sw))
                continue;

            int d = *depth;
            (*depth)++;

            char *field_name = to_snake_case(sw->field);
            char *field_type = find_field_data_type(elements, sw->field);

            char di_var[64];
            snprintf(di_var, sizeof(di_var), "__eolib_di_%d", d);

            fprintf(f, "    lua_getfield(L, %s, \"%s_data\");\n", idx_var, field_name);
            fprintf(f,
                "    if (lua_istable(L, -1)) { int %s = lua_gettop(L);"
                " %s%s%s_data = calloc(1, sizeof(*%s%s%s_data)); if (%s%s%s_data) {\n",
                di_var,
                out_expr, out_access, field_name,
                out_expr, out_access, field_name,
                out_expr, out_access, field_name);
            fprintf(f, "    switch (%s%s%s) {\n", out_expr, out_access, field_name);

            int has_default = 0;
            for (size_t c = 0; c < sw->cases_count; ++c)
            {
                CaseDef *case_def = &sw->cases[c];
                if (!case_def->has_elements || !element_list_has_storage(&case_def->elements))
                    continue;

                if (case_def->is_default)
                    has_default = 1;

                const char *case_name = case_def->is_default ? "default" : case_def->value;
                char *case_const = to_upper_snake(case_name);
                char *case_field = to_snake_case(case_name);

                if (case_def->is_default)
                {
                    fprintf(f, "    default: {\n");
                }
                else if (is_numeric_string(case_def->value))
                {
                    fprintf(f, "    case %s: {\n", case_def->value);
                }
                else if (field_type && enum_exists(enums, ec, field_type))
                {
                    char *enum_prefix = to_upper_snake(field_type);
                    fprintf(f, "    case %s_%s: {\n", enum_prefix, case_const);
                    free(enum_prefix);
                }
                else
                {
                    fprintf(f, "    case %s: {\n", case_def->value ? case_def->value : "0");
                }

                char new_out_expr[1024];
                snprintf(new_out_expr, sizeof(new_out_expr), "%s%s%s_data->%s",
                    out_expr, out_access, field_name, case_field);
                write_lua_to_c_elements(f, &case_def->elements, enums, ec, structs, sc,
                    di_var, new_out_expr, ".", depth);

                fprintf(f, "    break; }\n");
                free(case_const);
                free(case_field);
            }

            if (!has_default)
                fprintf(f, "    default: break;\n");
            fprintf(f, "    }\n");       /* close switch */
            fprintf(f, "    } }\n");     /* close if (_data) and if (lua_istable) */
            fprintf(f, "    lua_pop(L, 1);\n");

            free(field_name);
            free(field_type);
        }
        else if (element->kind == ELEMENT_CHUNKED)
        {
            write_lua_to_c_elements(f, &element->as.chunked, enums, ec, structs, sc,
                idx_var, out_expr, out_access, depth);
        }
        /* ELEMENT_BREAK, ELEMENT_COMMENT, ELEMENT_DUMMY, ELEMENT_LENGTH: skip */
    }
}

static void write_c_to_lua_elements(FILE *f, ElementList *elements, EnumDef *enums,
    size_t ec, StructDef *structs, size_t sc,
    const char *t_var, const char *val_expr, const char *val_access, int *depth)
{
    for (size_t i = 0; i < elements->elements_count; ++i)
    {
        StructElement *element = &elements->elements[i];

        if (element->kind == ELEMENT_FIELD)
        {
            FieldDef *field = &element->as.field;
            if (!field->name || field->value != NULL)
                continue;

            char *snake_name = to_snake_case(field->name);
            char *type_name = normalize_type_name(field->data_type);

            if (strcmp(type_name, "bool") == 0)
            {
                if (field->optional)
                {
                    fprintf(f,
                        "    if (%s%s%s) lua_pushboolean(L, *%s%s%s ? 1 : 0);"
                        " else lua_pushnil(L); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name,
                        val_expr, val_access, snake_name,
                        t_var, snake_name);
                }
                else
                {
                    fprintf(f,
                        "    lua_pushboolean(L, %s%s%s ? 1 : 0); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name, t_var, snake_name);
                }
            }
            else if (strcmp(type_name, "string") == 0 || strcmp(type_name, "encoded_string") == 0)
            {
                fprintf(f,
                    "    lua_pushstring(L, %s%s%s ? %s%s%s : \"\"); lua_setfield(L, %s, \"%s\");\n",
                    val_expr, val_access, snake_name,
                    val_expr, val_access, snake_name,
                    t_var, snake_name);
            }
            else if (strcmp(type_name, "blob") == 0)
            {
                fprintf(f,
                    "    lua_pushlstring(L, (const char*)%s%s%s, %s%s%s_length);"
                    " lua_setfield(L, %s, \"%s\");\n",
                    val_expr, val_access, snake_name,
                    val_expr, val_access, snake_name,
                    t_var, snake_name);
            }
            else if (enum_exists(enums, ec, type_name))
            {
                if (field->optional)
                {
                    fprintf(f,
                        "    if (%s%s%s) lua_pushinteger(L, (lua_Integer)*%s%s%s);"
                        " else lua_pushnil(L); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name,
                        val_expr, val_access, snake_name,
                        t_var, snake_name);
                }
                else
                {
                    fprintf(f,
                        "    lua_pushinteger(L, (lua_Integer)%s%s%s); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name, t_var, snake_name);
                }
            }
            else if (struct_exists(structs, sc, type_name))
            {
                if (struct_has_storage(structs, sc, type_name))
                {
                    if (field->optional)
                    {
                        fprintf(f,
                            "    if (%s%s%s) { %s_to_lua_table(L, %s%s%s); }"
                            " else { lua_pushnil(L); } lua_setfield(L, %s, \"%s\");\n",
                            val_expr, val_access, snake_name,
                            type_name, val_expr, val_access, snake_name,
                            t_var, snake_name);
                    }
                    else
                    {
                        fprintf(f,
                            "    %s_to_lua_table(L, &%s%s%s); lua_setfield(L, %s, \"%s\");\n",
                            type_name, val_expr, val_access, snake_name, t_var, snake_name);
                    }
                }
                /* struct without storage: skip */
            }
            else
            {
                /* primitive numeric */
                if (field->optional)
                {
                    fprintf(f,
                        "    if (%s%s%s) lua_pushinteger(L, (lua_Integer)*%s%s%s);"
                        " else lua_pushnil(L); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name,
                        val_expr, val_access, snake_name,
                        t_var, snake_name);
                }
                else
                {
                    fprintf(f,
                        "    lua_pushinteger(L, (lua_Integer)%s%s%s); lua_setfield(L, %s, \"%s\");\n",
                        val_expr, val_access, snake_name, t_var, snake_name);
                }
            }

            free(snake_name);
            free(type_name);
        }
        else if (element->kind == ELEMENT_ARRAY)
        {
            ArrayDef *array = &element->as.array;
            if (!array->name)
                continue;

            int d = *depth;
            (*depth)++;

            char *arr_name = to_snake_case(array->name);
            char *type_name = normalize_type_name(array->data_type);
            int is_static = is_static_length(array->length);

            int is_string_t = (strcmp(type_name, "string") == 0 ||
                               strcmp(type_name, "encoded_string") == 0);
            int is_enum_t = enum_exists(enums, ec, type_name);
            int is_struct_t = (!is_primitive_type(type_name) && !is_enum_t &&
                               struct_exists(structs, sc, type_name));

            fprintf(f, "    { lua_newtable(L); int __eolib_arrt_%d = lua_gettop(L);\n", d);

            if (is_static)
            {
                fprintf(f,
                    "    for (int __eolib_ai_%d = 0; __eolib_ai_%d < %s; __eolib_ai_%d++) {\n",
                    d, d, array->length, d);
            }
            else
            {
                fprintf(f,
                    "    for (size_t __eolib_ai_%d = 0; __eolib_ai_%d < %s%s%s_length; __eolib_ai_%d++) {\n",
                    d, d, val_expr, val_access, arr_name, d);
            }

            /* Push element value */
            if (is_string_t)
            {
                fprintf(f,
                    "    lua_pushstring(L, %s%s%s[__eolib_ai_%d] ? %s%s%s[__eolib_ai_%d] : \"\");\n",
                    val_expr, val_access, arr_name, d,
                    val_expr, val_access, arr_name, d);
            }
            else if (strcmp(type_name, "bool") == 0)
            {
                fprintf(f,
                    "    lua_pushboolean(L, %s%s%s[__eolib_ai_%d] ? 1 : 0);\n",
                    val_expr, val_access, arr_name, d);
            }
            else if (is_struct_t)
            {
                if (struct_has_storage(structs, sc, type_name))
                {
                    fprintf(f,
                        "    %s_to_lua_table(L, &%s%s%s[__eolib_ai_%d]);\n",
                        type_name, val_expr, val_access, arr_name, d);
                }
                else
                {
                    fprintf(f, "    lua_pushnil(L);\n");
                }
            }
            else
            {
                /* enum or primitive */
                fprintf(f,
                    "    lua_pushinteger(L, (lua_Integer)%s%s%s[__eolib_ai_%d]);\n",
                    val_expr, val_access, arr_name, d);
            }

            fprintf(f, "    lua_rawseti(L, __eolib_arrt_%d, (int)__eolib_ai_%d + 1); }\n", d, d);
            fprintf(f, "    lua_setfield(L, %s, \"%s\"); }\n", t_var, arr_name);

            free(arr_name);
            free(type_name);
        }
        else if (element->kind == ELEMENT_SWITCH)
        {
            SwitchDef *sw = &element->as.sw;
            if (!switch_has_storage(sw))
                continue;

            int d = *depth;
            (*depth)++;

            char *field_name = to_snake_case(sw->field);
            char *field_type = find_field_data_type(elements, sw->field);

            char dt_var[64];
            snprintf(dt_var, sizeof(dt_var), "__eolib_dt_%d", d);

            fprintf(f,
                "    if (%s%s%s_data) { lua_newtable(L); int %s = lua_gettop(L);\n",
                val_expr, val_access, field_name, dt_var);
            fprintf(f, "    switch (%s%s%s) {\n", val_expr, val_access, field_name);

            int has_default = 0;
            for (size_t c = 0; c < sw->cases_count; ++c)
            {
                CaseDef *case_def = &sw->cases[c];
                if (!case_def->has_elements || !element_list_has_storage(&case_def->elements))
                    continue;

                if (case_def->is_default)
                    has_default = 1;

                const char *case_name = case_def->is_default ? "default" : case_def->value;
                char *case_const = to_upper_snake(case_name);
                char *case_field = to_snake_case(case_name);

                if (case_def->is_default)
                {
                    fprintf(f, "    default: {\n");
                }
                else if (is_numeric_string(case_def->value))
                {
                    fprintf(f, "    case %s: {\n", case_def->value);
                }
                else if (field_type && enum_exists(enums, ec, field_type))
                {
                    char *enum_prefix = to_upper_snake(field_type);
                    fprintf(f, "    case %s_%s: {\n", enum_prefix, case_const);
                    free(enum_prefix);
                }
                else
                {
                    fprintf(f, "    case %s: {\n", case_def->value ? case_def->value : "0");
                }

                char new_val_expr[1024];
                snprintf(new_val_expr, sizeof(new_val_expr), "%s%s%s_data->%s",
                    val_expr, val_access, field_name, case_field);
                write_c_to_lua_elements(f, &case_def->elements, enums, ec, structs, sc,
                    dt_var, new_val_expr, ".", depth);

                fprintf(f, "    break; }\n");
                free(case_const);
                free(case_field);
            }

            if (!has_default)
                fprintf(f, "    default: break;\n");
            fprintf(f, "    }\n"); /* close switch */
            fprintf(f,
                "    lua_setfield(L, %s, \"%s_data\"); }"
                " else { lua_pushnil(L); lua_setfield(L, %s, \"%s_data\"); }\n",
                t_var, field_name, t_var, field_name);

            free(field_name);
            free(field_type);
        }
        else if (element->kind == ELEMENT_CHUNKED)
        {
            write_c_to_lua_elements(f, &element->as.chunked, enums, ec, structs, sc,
                t_var, val_expr, val_access, depth);
        }
        /* ELEMENT_BREAK, ELEMENT_COMMENT, ELEMENT_DUMMY, ELEMENT_LENGTH: skip */
    }
}

static void write_lua_enum_registration(FILE *f, EnumDef *def)
{
    fprintf(f, "static void lua_register_%s(lua_State *L, int module_idx)\n{\n", def->name);
    fprintf(f, "    lua_newtable(L);\n");
    for (size_t i = 0; i < def->values_count; ++i)
    {
        EnumValue *val = &def->values[i];
        fprintf(f, "    lua_pushinteger(L, %d); lua_setfield(L, -2, \"%s\");\n",
            val->value, val->name);
    }
    fprintf(f, "    lua_setfield(L, module_idx, \"%s\");\n}\n\n", def->name);
}

static void write_lua_type_bindings(FILE *f, const char *name, ElementList *elements,
    EnumDef *enums, size_t ec, StructDef *structs, size_t sc)
{
    int has_storage = element_list_has_storage(elements);
    int has_heap = element_list_has_heap(elements, enums, ec, structs, sc);

    if (has_storage)
    {
        /* lua_table_to_Name */
        fprintf(f, "static void lua_table_to_%s(lua_State *L, int idx, %s *out)\n{\n",
            name, name);
        fprintf(f, "    (void)L; (void)out;\n");
        fprintf(f, "    int __eolib_i; (void)__eolib_i;\n");
        int depth = 0;
        write_lua_to_c_elements(f, elements, enums, ec, structs, sc, "idx", "out", "->", &depth);
        fprintf(f, "}\n\n");

        /* Name_to_lua_table */
        fprintf(f, "static void %s_to_lua_table(lua_State *L, const %s *val)\n{\n", name, name);
        fprintf(f, "    lua_newtable(L); int __t = lua_gettop(L); (void)val;\n");
        depth = 0;
        write_c_to_lua_elements(f, elements, enums, ec, structs, sc, "__t", "val", "->", &depth);
        fprintf(f, "}\n\n");

        /* lua_Name_new */
        fprintf(f, "static int lua_%s_new(lua_State *L)\n{\n", name);
        fprintf(f, "    (void)L;\n");
        fprintf(f, "    lua_newtable(L);\n");
        fprintf(f, "    eolib_setmetatable(L, \"eolib.%s\");\n", name);
        fprintf(f, "    return 1;\n}\n\n");

        /* lua_Name_serialize */
        fprintf(f, "static int lua_%s_serialize(lua_State *L)\n{\n", name);
        fprintf(f, "    luaL_checktype(L, 1, LUA_TTABLE);\n");
        fprintf(f, "    %s __val;\n", name);
        fprintf(f, "    memset(&__val, 0, sizeof(__val));\n");
        fprintf(f, "    lua_table_to_%s(L, 1, &__val);\n", name);
        fprintf(f, "    EoWriter __writer = eo_writer_init_with_capacity(64);\n");
        fprintf(f, "    EoResult __r = %s_serialize(&__val, &__writer);\n", name);
        if (has_heap)
            fprintf(f, "    %s_free(&__val);\n", name);
        fprintf(f,
            "    if (__r != EO_SUCCESS) { eo_writer_free(&__writer);"
            " return luaL_error(L, \"serialize failed: %%s\", eo_result_string(__r)); }\n");
        fprintf(f, "    lua_pushlstring(L, (const char *)__writer.data, __writer.length);\n");
        fprintf(f, "    eo_writer_free(&__writer);\n");
        fprintf(f, "    return 1;\n}\n\n");

        /* lua_Name_deserialize */
        fprintf(f, "static int lua_%s_deserialize(lua_State *L)\n{\n", name);
        fprintf(f, "    size_t __len;\n");
        fprintf(f, "    const char *__data = luaL_checklstring(L, 1, &__len);\n");
        fprintf(f, "    EoReader __reader = eo_reader_init((const uint8_t *)__data, __len);\n");
        fprintf(f, "    %s __val;\n", name);
        fprintf(f, "    memset(&__val, 0, sizeof(__val));\n");
        fprintf(f, "    EoResult __r = %s_deserialize(&__val, &__reader);\n", name);
        fprintf(f,
            "    if (__r != EO_SUCCESS) { return luaL_error(L, \"deserialize failed: %%s\","
            " eo_result_string(__r)); }\n");
        fprintf(f, "    %s_to_lua_table(L, &__val);\n", name);
        fprintf(f, "    eolib_setmetatable(L, \"eolib.%s\");\n", name);
        if (has_heap)
            fprintf(f, "    %s_free(&__val);\n", name);
        fprintf(f, "    return 1;\n}\n\n");

        /* lua_Name_write */
        fprintf(f, "static int lua_%s_write(lua_State *L)\n{\n", name);
        fprintf(f, "    luaL_checktype(L, 1, LUA_TTABLE);\n");
        fprintf(f,
            "    LuaEoWriter *__uw = (LuaEoWriter *)luaL_checkudata(L, 2, EOLIB_WRITER_MT);\n");
        fprintf(f, "    %s __val;\n", name);
        fprintf(f, "    memset(&__val, 0, sizeof(__val));\n");
        fprintf(f, "    lua_table_to_%s(L, 1, &__val);\n", name);
        fprintf(f, "    EoResult __r = %s_serialize(&__val, &__uw->writer);\n", name);
        if (has_heap)
            fprintf(f, "    %s_free(&__val);\n", name);
        fprintf(f,
            "    if (__r != EO_SUCCESS) { return luaL_error(L, \"write failed: %%s\","
            " eo_result_string(__r)); }\n");
        fprintf(f, "    return 0;\n}\n\n");

        /* lua_register_Name */
        fprintf(f, "static void lua_register_%s(lua_State *L, int module_idx)\n{\n", name);
        fprintf(f, "    luaL_newmetatable(L, \"eolib.%s\");\n", name);
        fprintf(f, "    lua_newtable(L);\n");
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_serialize); lua_setfield(L, -2, \"serialize\");\n",
            name);
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_write); lua_setfield(L, -2, \"write\");\n",
            name);
        fprintf(f, "    lua_setfield(L, -2, \"__index\");\n");
        fprintf(f, "    lua_pop(L, 1);\n\n");
        fprintf(f, "    lua_newtable(L);\n");
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_new); lua_setfield(L, -2, \"new\");\n", name);
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_deserialize); lua_setfield(L, -2, \"deserialize\");\n",
            name);
        fprintf(f, "    lua_setfield(L, module_idx, \"%s\");\n}\n\n", name);
    }
    else
    {
        /* No storage variants */

        /* lua_Name_serialize */
        fprintf(f, "static int lua_%s_serialize(lua_State *L)\n{\n", name);
        fprintf(f, "    (void)L;\n");
        fprintf(f, "    EoWriter __writer = eo_writer_init_with_capacity(16);\n");
        fprintf(f, "    EoResult __r = %s_serialize(&__writer);\n", name);
        fprintf(f,
            "    if (__r != EO_SUCCESS) { eo_writer_free(&__writer);"
            " return luaL_error(L, \"serialize failed: %%s\", eo_result_string(__r)); }\n");
        fprintf(f, "    lua_pushlstring(L, (const char *)__writer.data, __writer.length);\n");
        fprintf(f, "    eo_writer_free(&__writer);\n");
        fprintf(f, "    return 1;\n}\n\n");

        /* lua_Name_deserialize */
        fprintf(f, "static int lua_%s_deserialize(lua_State *L)\n{\n", name);
        fprintf(f, "    size_t __len;\n");
        fprintf(f, "    const char *__data = luaL_checklstring(L, 1, &__len);\n");
        fprintf(f, "    EoReader __reader = eo_reader_init((const uint8_t *)__data, __len);\n");
        fprintf(f, "    EoResult __r = %s_deserialize(&__reader);\n", name);
        fprintf(f,
            "    if (__r != EO_SUCCESS) { return luaL_error(L, \"deserialize failed: %%s\","
            " eo_result_string(__r)); }\n");
        fprintf(f, "    lua_pushboolean(L, 1);\n");
        fprintf(f, "    return 1;\n}\n\n");

        /* lua_register_Name */
        fprintf(f, "static void lua_register_%s(lua_State *L, int module_idx)\n{\n", name);
        fprintf(f, "    lua_newtable(L);\n");
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_serialize); lua_setfield(L, -2, \"serialize\");\n",
            name);
        fprintf(f,
            "    lua_pushcfunction(L, lua_%s_deserialize); lua_setfield(L, -2, \"deserialize\");\n",
            name);
        fprintf(f, "    lua_setfield(L, module_idx, \"%s\");\n}\n\n", name);
    }
}

static void write_lua_files(ProtocolDef *protocols, size_t protocol_count)
{
    ensure_dir("lua");
    ensure_dir("lua/src");

    FILE *header = fopen("lua/src/lua_protocol.h", "w");
    FILE *source = fopen("lua/src/lua_protocol.c", "w");

    if (!header || !source)
    {
        fprintf(stderr, "Failed to open lua protocol output files\n");
        if (header)
            fclose(header);
        if (source)
            fclose(source);
        return;
    }

    /* Write header */
    fprintf(header, "%s", CODEGEN_WARNING);
    fprintf(header, "#ifndef EOLIB_LUA_PROTOCOL_H\n#define EOLIB_LUA_PROTOCOL_H\n\n");
    fprintf(header, "#include <lua.h>\n\n");
    fprintf(header, "void lua_protocol_register(lua_State *L, int module_idx);\n\n");
    fprintf(header, "#endif /* EOLIB_LUA_PROTOCOL_H */\n");
    fclose(header);

    /* Collect all enums and structs (deduplicated) */
    EnumDef *all_enums = NULL;
    size_t all_enums_count = 0, all_enums_capacity = 0;
    StructDef *all_structs = NULL;
    size_t all_structs_count = 0, all_structs_capacity = 0;
    StringList enum_names = {0}, struct_names = {0};

    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        for (size_t i = 0; i < protocol->enums_count; ++i)
        {
            if (!string_list_contains(&enum_names, protocol->enums[i].name))
            {
                enum_list_push(&all_enums, &all_enums_count, &all_enums_capacity,
                    &protocol->enums[i]);
                string_list_push(&enum_names, protocol->enums[i].name);
            }
        }
        for (size_t i = 0; i < protocol->structs_count; ++i)
        {
            if (!string_list_contains(&struct_names, protocol->structs[i].name))
            {
                struct_list_push(&all_structs, &all_structs_count, &all_structs_capacity,
                    &protocol->structs[i]);
                string_list_push(&struct_names, protocol->structs[i].name);
            }
        }
    }

    /* Write source file header */
    fprintf(source, "%s", CODEGEN_WARNING);
    fprintf(source, "#include \"lua_protocol.h\"\n");
    fprintf(source, "#include \"eolib_lua.h\"\n");
    fprintf(source, "#include \"protocol.h\"\n");
    fprintf(source, "#include \"result.h\"\n");
    fprintf(source, "#include \"data.h\"\n");
    fprintf(source, "#include <stdlib.h>\n#include <string.h>\n\n");

    /* Write forward declarations for all types with storage */
    StringList written_fwd = {0};

    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        for (size_t i = 0; i < protocol->structs_count; ++i)
        {
            const char *sname = protocol->structs[i].name;
            if (string_list_contains(&written_fwd, sname))
                continue;
            if (element_list_has_storage(&protocol->structs[i].elements))
            {
                fprintf(source,
                    "static void lua_table_to_%s(lua_State *L, int idx, %s *out);\n",
                    sname, sname);
                fprintf(source,
                    "static void %s_to_lua_table(lua_State *L, const %s *val);\n",
                    sname, sname);
            }
            string_list_push(&written_fwd, sname);
        }
    }

    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        const char *source_suffix = "Client";
        if (strstr(protocol->path, "/server/"))
            source_suffix = "Server";
        else if (strstr(protocol->path, "/client/"))
            source_suffix = "Client";

        for (size_t i = 0; i < protocol->packets_count; ++i)
        {
            char name_buffer[256];
            snprintf(name_buffer, sizeof(name_buffer), "%s%s%sPacket",
                protocol->packets[i].family, protocol->packets[i].action, source_suffix);
            if (string_list_contains(&written_fwd, name_buffer))
                continue;
            if (element_list_has_storage(&protocol->packets[i].elements))
            {
                fprintf(source,
                    "static void lua_table_to_%s(lua_State *L, int idx, %s *out);\n",
                    name_buffer, name_buffer);
                fprintf(source,
                    "static void %s_to_lua_table(lua_State *L, const %s *val);\n",
                    name_buffer, name_buffer);
            }
            string_list_push(&written_fwd, name_buffer);
        }
    }
    fprintf(source, "\n");

    /* Write enum registrations */
    StringList written_enums = {0};
    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        for (size_t i = 0; i < protocol->enums_count; ++i)
        {
            if (string_list_contains(&written_enums, protocol->enums[i].name))
                continue;
            write_lua_enum_registration(source, &protocol->enums[i]);
            string_list_push(&written_enums, protocol->enums[i].name);
        }
    }

    /* Write struct bindings */
    StringList written_structs = {0};
    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        for (size_t i = 0; i < protocol->structs_count; ++i)
        {
            if (string_list_contains(&written_structs, protocol->structs[i].name))
                continue;
            write_lua_type_bindings(source, protocol->structs[i].name,
                &protocol->structs[i].elements,
                all_enums, all_enums_count, all_structs, all_structs_count);
            string_list_push(&written_structs, protocol->structs[i].name);
        }
    }

    /* Write packet bindings */
    StringList written_packets = {0};
    for (size_t p = 0; p < protocol_count; ++p)
    {
        ProtocolDef *protocol = &protocols[p];
        const char *source_suffix = "Client";
        if (strstr(protocol->path, "/server/"))
            source_suffix = "Server";
        else if (strstr(protocol->path, "/client/"))
            source_suffix = "Client";

        for (size_t i = 0; i < protocol->packets_count; ++i)
        {
            char name_buffer[256];
            snprintf(name_buffer, sizeof(name_buffer), "%s%s%sPacket",
                protocol->packets[i].family, protocol->packets[i].action, source_suffix);
            if (string_list_contains(&written_packets, name_buffer))
                continue;
            write_lua_type_bindings(source, name_buffer,
                &protocol->packets[i].elements,
                all_enums, all_enums_count, all_structs, all_structs_count);
            string_list_push(&written_packets, name_buffer);
        }
    }

    /* Write lua_protocol_register */
    fprintf(source, "void lua_protocol_register(lua_State *L, int module_idx)\n{\n");
    for (size_t i = 0; i < written_enums.count; ++i)
        fprintf(source, "    lua_register_%s(L, module_idx);\n", written_enums.items[i]);
    for (size_t i = 0; i < written_structs.count; ++i)
        fprintf(source, "    lua_register_%s(L, module_idx);\n", written_structs.items[i]);
    for (size_t i = 0; i < written_packets.count; ++i)
        fprintf(source, "    lua_register_%s(L, module_idx);\n", written_packets.items[i]);
    fprintf(source, "}\n");

    fclose(source);

    free(all_enums);
    free(all_structs);
}


static void write_lua_doc_comment(FILE *f, const char *comment)
{
    if (!comment || comment[0] == '\0') return;
    char *copy = xstrdup(comment);
    char *line = strtok(copy, "\n");
    while (line)
    {
        char *start = line;
        while (*start && isspace((unsigned char)*start)) start++;
        char *end = start + strlen(start);
        while (end > start && isspace((unsigned char)*(end - 1))) end--;
        *end = '\0';
        if (start[0] != '\0')
            fprintf(f, "--- %s\n", start);
        line = strtok(NULL, "\n");
    }
    free(copy);
}

/* =========================================================================
 * LuaCATS annotation generation (eolib.d.lua for lua-language-server)
 * ======================================================================= */

/* Map a protocol data_type string to a Lua type annotation string */
static const char *lua_cats_type(const char *data_type,
    EnumDef *enums, size_t enums_count,
    StructDef *structs, size_t structs_count)
{
    if (!data_type) return "any";
    if (strcmp(data_type, "byte")   == 0 ||
        strcmp(data_type, "char")   == 0 ||
        strcmp(data_type, "short")  == 0 ||
        strcmp(data_type, "three")  == 0 ||
        strcmp(data_type, "int")    == 0) return "integer";
    if (strcmp(data_type, "string")               == 0 ||
        strcmp(data_type, "encoded_string")       == 0 ||
        strcmp(data_type, "fixed_string")         == 0 ||
        strcmp(data_type, "fixed_encoded_string") == 0 ||
        strcmp(data_type, "blob")                 == 0) return "string";
    if (strcmp(data_type, "bool") == 0) return "boolean";
    for (size_t i = 0; i < enums_count; ++i)
        if (strcmp(enums[i].name, data_type) == 0) return data_type;
    for (size_t i = 0; i < structs_count; ++i)
        if (strcmp(structs[i].name, data_type) == 0) return data_type;
    return "any";
}

static void write_cats_elements(FILE *f, ElementList *elements,
    EnumDef *enums, size_t enums_count,
    StructDef *structs, size_t structs_count)
{
    for (size_t i = 0; i < elements->elements_count; ++i)
    {
        StructElement *el = &elements->elements[i];
        if (el->kind == ELEMENT_FIELD)
        {
            FieldDef *fd = &el->as.field;
            if (!fd->name) continue;
            const char *t = lua_cats_type(fd->data_type, enums, enums_count, structs, structs_count);
            if (fd->comment) write_lua_doc_comment(f, fd->comment);
            fprintf(f, "---@field %s %s%s\n", fd->name, t, fd->optional ? "?" : "");
        }
        else if (el->kind == ELEMENT_ARRAY)
        {
            ArrayDef *ad = &el->as.array;
            if (!ad->name) continue;
            const char *t = lua_cats_type(ad->data_type, enums, enums_count, structs, structs_count);
            if (ad->comment) write_lua_doc_comment(f, ad->comment);
            fprintf(f, "---@field %s %s[]%s\n", ad->name, t, ad->optional ? "?" : "");
        }
        else if (el->kind == ELEMENT_SWITCH)
        {
            SwitchDef *sd = &el->as.sw;
            fprintf(f, "---@field %s_data table?\n", sd->field);
        }
        else if (el->kind == ELEMENT_CHUNKED)
        {
            write_cats_elements(f, &el->as.chunked,
                enums, enums_count, structs, structs_count);
        }
    }
}

