static char *normalize_type_name(const char *data_type)
{
    if (!data_type)
    {
        return xstrdup("");
    }
    const char *colon = strchr(data_type, ':');
    if (!colon)
    {
        return xstrdup(data_type);
    }
    size_t len = (size_t)(colon - data_type);
    char *out = xmalloc(len + 1);
    memcpy(out, data_type, len);
    out[len] = '\0';
    return out;
}

/* Returns the type override from "EnumName:overridetype", or NULL if none.
   Caller must free the returned string. */
static char *get_type_override(const char *data_type)
{
    if (!data_type)
        return NULL;
    const char *colon = strchr(data_type, ':');
    if (!colon)
        return NULL;
    return xstrdup(colon + 1);
}

static char *normalize_path(const char *path)
{
    size_t len = strlen(path);
    char *out = xmalloc(len + 1);
    for (size_t i = 0; i < len; ++i)
    {
        char ch = path[i];
        out[i] = ch == '\\' ? '/' : ch;
    }
    out[len] = '\0';
    return out;
}

static char *relative_protocol_dir(const char *path)
{
    char *normalized = normalize_path(path);
    const char *needle = "/eo-protocol/xml/";
    char *pos = strstr(normalized, needle);
    if (!pos)
    {
        free(normalized);
        return xstrdup("");
    }
    pos += strlen(needle);
    char *last_slash = strrchr(pos, '/');
    if (!last_slash)
    {
        free(normalized);
        return xstrdup("");
    }
    size_t len = (size_t)(last_slash - pos);
    char *out = xmalloc(len + 1);
    memcpy(out, pos, len);
    out[len] = '\0';
    free(normalized);
    return out;
}

static int ensure_dir(const char *path)
{
    char temp[1024];
    size_t len = strlen(path);
    if (len >= sizeof(temp))
    {
        return -1;
    }
    snprintf(temp, sizeof(temp), "%s", path);
    if (temp[len - 1] == '/')
    {
        temp[len - 1] = '\0';
    }

    for (char *p = temp + 1; *p; ++p)
    {
        if (*p == '/')
        {
            *p = '\0';
            if (eolib_mkdir(temp) != 0 && errno != EEXIST)
            {
                return -1;
            }
            *p = '/';
        }
    }

    if (eolib_mkdir(temp) != 0 && errno != EEXIST)
    {
        return -1;
    }
    return 0;
}

